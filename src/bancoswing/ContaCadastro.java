    private void cbContaTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbContaTipoItemStateChanged
        if (cbContaTipo.getSelectedItem().toString().equals(conta_corrente)) {
            jLabel3.setVisible(true);
            txtContaLimite.setVisible(true);

            jLabel6.setVisible(false);
            txtContaMontante.setVisible(false);
            jLabel7.setVisible(false);
            txtContaDepositoMin.setVisible(false);

            btContaSalvar.setEnabled(true);

        } else if (cbContaTipo.getSelectedItem().toString().equals(conta_investimento)) {
            jLabel3.setVisible(false);
            txtContaLimite.setVisible(false);

            jLabel6.setVisible(true);
            txtContaMontante.setVisible(true);
            jLabel7.setVisible(true);
            txtContaDepositoMin.setVisible(true);

            btContaSalvar.setEnabled(true);

        } else {
            Limpa();
        }
    }//GEN-LAST:event_cbContaTipoItemStateChanged

